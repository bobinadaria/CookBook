const supabase = createClient(SUPABASE_URL, SERVICE_ROLE_KEY);

// ── Helpers ───────────────────────────────────────────────────────────────────

function toSlug(title) {
  const CYRILLIC_MAP = {
    а:"a",б:"b",в:"v",г:"g",д:"d",е:"e",ё:"yo",ж:"zh",з:"z",и:"i",й:"y",
    к:"k",л:"l",м:"m",н:"n",о:"o",п:"p",р:"r",с:"s",т:"t",у:"u",ф:"f",
    х:"kh",ц:"ts",ч:"ch",ш:"sh",щ:"shch",ъ:"",ы:"y",ь:"",э:"e",ю:"yu",я:"ya",
  };
  return title.toLowerCase().split("").map((ch) => CYRILLIC_MAP[ch] ?? ch).join("")
    .replace(/[^a-z0-9\s-]/g, "").trim().replace(/\s+/g, "-").replace(/-+/g, "-");
}

async function upsertCategory(name, nameEn, type) {
  const slug = toSlug(name);
  const { data: existing } = await supabase
    .from("categories").select("id").eq("slug", slug).maybeSingle();

  if (existing) {
    console.log(`  ✓ category "${name}" already exists (${existing.id})`);
    return existing.id;
  }

  const { data, error } = await supabase
    .from("categories")
    .insert({ name, name_en: nameEn, slug, type })
    .select("id").single();

  if (error) throw new Error(`Category insert failed: ${error.message}`);
  console.log(`  + created category "${name}" (${data.id})`);
  return data.id;
}

async function insertRecipe({ title, description, note = null, ingredients, cook_time, servings, featured = false }) {
  const slug = toSlug(title);

  const { data: existing } = await supabase
    .from("recipes").select("id, slug").eq("slug", slug).maybeSingle();

  if (existing) {
    console.log(`  ✓ recipe "${title}" already exists (${existing.id})`);
    return existing;
  }

  const { data, error } = await supabase
    .from("recipes")
    .insert({
      title,
      slug,
      description,
      note,
      ingredients,
      published: true,
      featured,
      cook_time,
      servings,
    })
    .select("id, slug")
    .single();

  if (error) throw new Error(`Recipe insert failed for "${title}": ${error.message}`);
  console.log(`  + created recipe "${title}"`);
  console.log(`    slug: ${data.slug}`);
  return data;
}

async function linkCategories(recipeId, categoryIds) {
  const inserts = categoryIds.map((id) => ({ recipe_id: recipeId, category_id: id }));
  const { error } = await supabase.from("recipe_categories").insert(inserts);
  if (error) throw new Error(`Category link failed: ${error.message}`);
  console.log(`    → linked ${categoryIds.length} categor${categoryIds.length === 1 ? "y" : "ies"}`);
}

async function insertSteps(recipeId, steps) {
  const { error } = await supabase.from("steps").insert(
    steps.map((s) => ({ recipe_id: recipeId, ...s }))
  );
  if (error) throw new Error(`Steps insert failed: ${error.message}`);
  console.log(`    → inserted ${steps.length} steps`);
}

