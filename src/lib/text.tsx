import { Fragment, type ReactNode } from "react";

/**
 * Не даёт браузеру переносить строку ПРЯМО НА дефисе внутри слова (например
 * «пти-шу» иначе ломается на «пти-» / «шу» на двух строках — дефис по
 * умолчанию считается точкой разрыва). Дефисные слова оборачиваем в
 * `whitespace-nowrap`: слово остаётся целым и при нехватке места переносится
 * на новую строку ВСЕМ словом, а не разрывается по дефису.
 *
 * Используется везде, где рендерятся заголовки рецептов (карточки, страница
 * рецепта, личная книга) — правило сквозное для дизайна, не точечное.
 */
export function noBreakHyphens(text: string): ReactNode {
  return text.split(" ").map((word, i) => (
    <Fragment key={i}>
      {i > 0 && " "}
      {word.includes("-") ? <span className="whitespace-nowrap">{word}</span> : word}
    </Fragment>
  ));
}
